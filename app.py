@app.route("/preview", methods=["POST"])
def preview():
    f = request.files.get("file")
    if not f:
        return jsonify({"error": "파일이 없습니다"}), 400
    raw = f.read()
    try:
        csv_rows = parse_csv(raw)
    except Exception as e:
        return jsonify({"error": f"CSV 파싱 오류: {str(e)}"}), 400

    rows, warnings, filename = convert_rows(csv_rows)
    return jsonify({"columns": HEADER_EN, "rows": rows, "warnings": warnings, "filename": filename})


