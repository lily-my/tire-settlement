def convert_rows(csv_rows):
    warnings = []
    output_rows = []
    filename = '타이어입고등록.xlsx'

    data = csv_rows[1:] if len(csv_rows) > 1 else []
    if not data:
        return [], [], filename

    try:
        vendor = data[0][18].strip()
        if vendor:
            filename = vendor + ' 정산.xlsx'
    except (IndexError, AttributeError):
        pass

    missing_tr = set()
    missing_lc = set()

    for row in data:
        if len(row) < 20:
            continue
        try:
            발생일 = row[2].strip()
            예약id = row[3].strip()
            장소id = row[5].strip()
            장소명 = row[6].strip()
            상품명 = row[7].strip()
            사이즈 = row[8].strip()
            수량 = int(float(row[11].strip() or '0'))
            합계금액 = int(float(row[16].strip() or '0'))
            최종도매처 = row[18].strip()
        except (ValueError, IndexError):
            continue

        rcv_dt = 발생일.replace('-', '')
        tr_cd = TR_MAP.get(최종도매처) or CUSTOM_TR.get(최종도매처, '')
        lc_cd = LC_MAP.get(장소id) or CUSTOM_LC.get(장소id, '')

        if not tr_cd:
            missing_tr.add(최종도매처)
        if not lc_cd:
            missing_lc.add(f'{장소명}({장소id})')

        rcvh_am = 합계금액
        rcvv_am = round(rcvh_am / 11)
        rcvg_am = rcvh_am - rcvv_am

        output_rows.append([
            '0', rcv_dt, tr_cd, 'KRW', 1, '0', '0', 'A001',
            f'{rcv_dt}_{최종도매처}_배송장착',
            f'{상품명}_{사이즈}',
            수량, 수량, rcvg_am, rcvg_am, rcvv_am, rcvh_am,
            lc_cd,
            f'{예약id} / {장소명} ({장소id}) / 배송장착',
        ])

    for nm in sorted(missing_tr):
        warnings.append(f'거래처코드 없음: {nm}')
    for nm in sorted(missing_lc):
        warnings.append(f'장소코드 없음: {nm}')

    return output_rows, warnings, filename


def _push_to_github(data: dict):
    if not GITHUB_TOKEN:
        return
    try:
        headers = {
            'Authorization': f'token {GITHUB_TOKEN}',
            'Accept': 'application/vnd.github.v3+json',
        }
        url = f'https://api.github.com/repos/{GITHUB_REPO}/contents/{CUSTOM_CODES_FILE}'
        r = http_req.get(url, headers=headers, timeout=10)
        sha = r.json().get('sha') if r.ok else None

        content = json.dumps(data, ensure_ascii=False, indent=2)
        payload = {
            'message': f'코드 추가: TR {len(data["TR_MAP"])}건 LC {len(data["LC_MAP"])}건',
            'content': base64.b64encode(content.encode()).decode(),
        }
        if sha:
            payload['sha'] = sha
        http_req.put(url, headers=headers, json=payload, timeout=10)
    except Exception as e:
        print(f'GitHub push 실패: {e}')


